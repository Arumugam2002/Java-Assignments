/* Write a program in Java to display the first 10 natural numbers 
using while loop         */


public class Q_3_1_First10NaturalNumbersUsingWhileLoop {
    
	
	public static void main(String[] args) {
		
        int i = 1;  
        while (i <= 10) {  
            System.out.println(i);  
            i++;  
        }
    }
}

